function Elevação_3(){
    var n1 = parseInt(prompt("Digite um número"))
    var n2 = parseInt(prompt("Digite outro número"))
    var n3 = parseInt(prompt("Digite o último"))
    var Result = parseFloat((n1 * n1) + (n2 * n2) + (n3 * n3))

    alert("A soma dos valores elevado ao quadrado é: " + Result)
}